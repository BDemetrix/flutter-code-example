/// Типы операций над данными.
///
/// Используются для определения того, что делать с данными.
enum Operation {
  /// Инициализация данных после успешного логина.
  initialize,

  /// Добавление новых объектов в ходе сессии.
  add,

  /// Удаление объектов.
  remove,

  /// Изменение существующих объектов.
  change,
}
